     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validator = Validator::make(
            $request->all(),
            [
                "name" => 'required',
            ]
        );

        if ($validator->fails()) {
            return $validator->messages();
        }

        Work::create([
            'name' => $request->name,
            'image' => $request->image,
            'link' => $request->link,
            'url' => $request->url,
            'sort' => $request->sort,
        ]);
    }

